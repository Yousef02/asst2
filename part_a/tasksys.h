        void sync();

    private:
        std::vector<std::thread> workers;
        IRunnable *runnable;
        std::mutex tasks_l;
        // std::unique_lock<std::mutex> tasks_l;
        int num_total_tasks;
        int num_threads;
        int task_id;
        int in_progress;
        bool spin;
        std::condition_variable cv;
        bool done;

        std::mutex dbg_l;
